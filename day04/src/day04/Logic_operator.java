package day04;
//논리부정연산자
public class Logic_operator {
	public static void main(String[] args) {
		boolean a=true; //a변수에 true값 저장
		boolean b=false; //b변수에 false값 저장
		boolean c=!b; // c변수에 b 가 true라면 false, false라면 true

		System.out.println(a);
		System.out.println(!a);
		
		System.out.println(b);
		System.out.println(c);
	}

}
